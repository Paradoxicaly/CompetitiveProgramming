void solve() {
    int x1, y1, x2, y2, x3, y3;
    cin >> x1 >> y1 >> x2 >> y2 >> x3 >> y3;
    bool ok = 0;
    if (x1 == x2 && x2 == x3 && y1 + y2 + y3 == x1) ok = 1;
    if (y1 == y2 && y2 == y3 && x1 + x2 + x3 == y1) ok = 1;
    if (x2 == x3 && y2 + y3 == y1 && x1 + x2 == y1) ok = 1;
    if (y2 == y3 && x2 + x3 == x1 && y1 + y2 == x1) ok = 1;
    cout << (ok ? "YES" : "NO") << '\n';
}

